#ทั้งนี้เงินเดือนสุทธิของพนักงานนั้นจะต้องหักภาษีและเบี้ยประกันสังคมออกจากเงินเดือนปกติเสียก่อนโดยที่พนักงานต้องเสียภาษี 7% ของเงินเดือนปกติและจ่ายค่าเบี้ยประกันสังคม 500
