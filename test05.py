def A():
    id = input("ป้อนรหัสพนักงาน : ")
    name= input("ป้อนชื่อพนักงาน : ")
    ml=float(input("ป้อนเงินปกติของพนักงาน"))
    return id, name, ml
def B(ml):
    pc=7
    tax=(ml*pc)/100
    return tax
def C():
    ssf=500
    return ssf
def D(ml, tax,ssf):
    ns=ml-tax-ssf
    return ns
def E(id,name,ml,tax,ssf,ns):
    print(f"ป้อนรหัสพนักงาน :{id}")
    print(f"ป้อนชื่อพนักงาน :{name}")
    print(f"ป้อนเงินเดือนพนักงาน :{ml:.2f}")
    print(f"ภาษีเงิน :{tax:.2f}")
    print(f"เบื้ยประกันสังคม :{ssf:.2f}")
    print(f"เงินเดือนสุทธิ :{ns:.2f}")
print("==========================================")
print("=========คำนวณเงินเดือนพนักงาน==============")
print("==========================================")
id,name,ml = A()
print("==========================================")
